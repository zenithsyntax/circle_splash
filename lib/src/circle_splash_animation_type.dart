/// Enum defining the different types of circle animations available
enum CircleSplashAnimationType {
  /// Circle expands from the center of the screen
  center,
  
  /// Circle expands from the top-left corner
  topLeft,
  
  /// Circle expands from the top-right corner
  topRight,
  
  /// Circle expands from the bottom-left corner
  bottomLeft,
  
  /// Circle expands from the bottom-right corner
  bottomRight,
}